package PBO_SMT_3.Latihan.Pertemuan6;

public class AnggotaKampus {

    String nama;
    String id;

    AnggotaKampus(String nama, String id) {
        this.nama = nama;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

}